ALTER TABLE `COMENTS` ADD CONSTRAINT `COMENTS_FK0` FOREIGN KEY (`USER_ID`) REFERENCES `USERS`(`ID`);
ALTER TABLE `COMENTS` ADD CONSTRAINT `COMENTS_FK1` FOREIGN KEY (`POST_ID`) REFERENCES `POSTS`(`ID`);

